export const getOverviewStats = (enterprises) => {
  const totalInvoice = enterprises.reduce((sum, e) => sum + e.annualInvoiceAmount, 0);
  const totalSales = enterprises.reduce((sum, e) => sum + e.annualSales, 0);
  const totalPurchase = enterprises.reduce((sum, e) => sum + e.annualPurchase, 0);
  
  return {
    totalCount: enterprises.length,
    totalInvoiceAmount: totalInvoice,
    totalSales,
    totalPurchase,
  };
};
